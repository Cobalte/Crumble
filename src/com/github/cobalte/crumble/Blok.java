package com.github.cobalte.crumble;

import org.bukkit.Material;

public class Blok {

	// ----------------------------------------------------------------------------------------------------------------\\
	//    PRIVATE VARS
	// ----------------------------------------------------------------------------------------------------------------//
	
	private Material mat;
	
	// ----------------------------------------------------------------------------------------------------------------\\
	//    PUBLIC METHODS
	// ----------------------------------------------------------------------------------------------------------------//
	
	public Material getMaterial() {
		return mat;
	}
	
	// ----------------------------------------------------------------------------------------------------------------\\
	//    CONSTRUCTORS
	// ----------------------------------------------------------------------------------------------------------------//
	
	public Blok() {
		mat = null;
	}
	
	public Blok(Material material) {
		mat = material;
	}
	
}
